def text(x, y, text_str, col=7, shadow=1):
    pyxel.text(x, y + 1, text_str, shadow)
    pyxel.text(x, y, text_str, col)


def text_label(x, y, text_str, col=7, shadow=1, bg_col=0):
    pyxel.rect(
        x,
        y,
        get_str_width(text_str),
        pyxel.FONT_HEIGHT,
        bg_col
    )
    text(x, y, text_str, col, shadow)


def get_str_width(text_str):
    return len(text_str) * pyxel.FONT_WIDTH
